public class primitivosenteros {
    public static void main(String[] args) {
        byte numeroByte = 7;
        System.out.println("Valor maximo de un Byte: " + Byte.MAX_VALUE); // maximo valor de un Byte
        System.out.println("Valor minimo de un Byte: " + Byte.MIN_VALUE); // minimo valor de un Byte

        short numeroShort = 8;
        System.out.println("Valor maximo de un short: " + Short.MAX_VALUE); // maximo de un short
        System.out.println("Valor minimo de un short: " + Short.MIN_VALUE); // minimo de un short

        int numeroEntero = 9;
        System.out.println("Valor maximo de un int: " + Integer.MAX_VALUE); // maximo valor de un int
        System.out.println("Valor minimo de un int: " + Integer.MIN_VALUE); // minimo valor de un int

        long numeroLong = 10L;
        System.out.println("Valor maximo de un long: " + Long.MAX_VALUE); // maximo valor de un long
        System.out.println("Valor minimo de un long: " + Long.MIN_VALUE); // minimo valor de un long
    }
}
